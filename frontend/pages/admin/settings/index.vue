<script setup lang="ts">
definePageMeta({
  layout: "admin",
  middleware: ["auth", "admin"],
});

const config = useRuntimeConfig();
const toast = useCustomToast();
const adminStore = useAdminStore();

const isLoading = ref(true);
const isSaving = ref(false);
const isRegenerating = ref(false);
const webhookToken = ref("");

const settings = reactive({
  platformName: "MaticTrade",
  supportEmail: "support@matictrade.com",
  maintenanceMode: false,
  emailNotifications: true,
  telegramNotifications: false,
});

// Social media links
const socialLinks = reactive({
  telegram: "",
  contactTelegram: "",
  twitter: "",
  instagram: "",
  facebook: "",
  youtube: "",
  discord: "",
});

// Homepage settings
const homepageSettings = reactive({
  watchDemoLink: "",
});

// System info for sidebar display
const systemInfo = reactive({
  version: "1.0.0",
  timestamp: new Date().toISOString(),
  environment: process.env.NODE_ENV || "development",
});

// Helper functions
const formatLastUpdated = (timestamp: string): string => {
  try {
    const date = new Date(timestamp);
    return date.toLocaleDateString("en-US", {
      year: "numeric",
      month: "short",
      day: "numeric",
    });
  } catch {
    return "Unknown";
  }
};

const capitalizeFirst = (str: string): string => {
  if (!str) return "";
  return str.charAt(0).toUpperCase() + str.slice(1);
};

const webhookFormat = `{"action": "open", "symbol": "BTCUSDT", "side": "long"}`;

// New simplified webhook format examples
const formatOpen = `{"action": "open", "symbol": "BTCUSDT", "side": "long"}`;
const formatClose = `{"action": "close", "symbol": "BTCUSDT", "side": "long"}`;
const formatSLClose = `{"action": "slclose", "symbol": "BTCUSDT", "side": "long"}`;
const formatTPClose = `{"action": "tpclose", "symbol": "BTCUSDT", "side": "long"}`;
const formatBEP = `{"action": "bep", "symbol": "BTCUSDT", "side": "long"}`;

// Generate webhook URL - uses NUXT_PUBLIC_API_BASE_URL from env
const webhookUrl = computed(() => {
  const baseUrl = config.public.apiBaseUrl || "http://localhost:3001";
  return `${baseUrl}/api/webhook/system/${webhookToken.value || "loading..."}`;
});

const copyWebhook = async () => {
  const textToCopy = webhookUrl.value;

  // Try using the modern Clipboard API first
  if (navigator.clipboard && window.isSecureContext) {
    try {
      await navigator.clipboard.writeText(textToCopy);
      toast.success("Copied!", "Webhook URL copied to clipboard");
      return;
    } catch (err) {
      // Fall through to fallback method
    }
  }

  // Fallback method using textarea
  try {
    const textArea = document.createElement("textarea");
    textArea.value = textToCopy;
    textArea.style.position = "fixed";
    textArea.style.left = "-999999px";
    textArea.style.top = "-999999px";
    document.body.appendChild(textArea);
    textArea.focus();
    textArea.select();

    const successful = document.execCommand("copy");
    document.body.removeChild(textArea);

    if (successful) {
      toast.success("Copied!", "Webhook URL copied to clipboard");
    } else {
      toast.error("Error", "Failed to copy - please copy manually");
    }
  } catch (error) {
    toast.error("Error", "Failed to copy - please copy manually");
  }
};

const regenerateWebhook = async () => {
  if (
    !confirm(
      "Are you sure? All existing TradingView alerts will need to be updated."
    )
  )
    return;

  isRegenerating.value = true;
  try {
    const result = await adminStore.regenerateWebhookToken();
    if (result?.webhookToken) {
      webhookToken.value = result.webhookToken;
      toast.success("Regenerated", "New webhook URL generated");
    }
  } catch (error: any) {
    toast.error("Error", error.message || "Failed to regenerate webhook");
  } finally {
    isRegenerating.value = false;
  }
};

const saveSettings = async () => {
  isSaving.value = true;
  try {
    await adminStore.updateSystemSettings({
      platformName: settings.platformName,
      supportEmail: settings.supportEmail,
      maintenanceMode: settings.maintenanceMode,
      emailNotifications: settings.emailNotifications,
      telegramNotifications: settings.telegramNotifications,
    });
    
    // Also save social links
    await saveSocialLinks();
    
    // Also save homepage settings
    await saveHomepageSettings();
    
    toast.success("Settings saved successfully");
  } catch (error: any) {
    toast.error(error.message || "Failed to save settings");
  } finally {
    isSaving.value = false;
  }
};

const clearAllCaches = async () => {
  if (!confirm("Are you sure you want to clear all caches?")) return;

  try {
    await adminStore.clearCache();
    toast.success("Cache cleared successfully");
  } catch (error: any) {
    toast.error(error.message || "Failed to clear cache");
  }
};

const resetSystemSettings = () => {
  if (
    !confirm("Are you sure you want to reset all system settings to defaults?")
  )
    return;

  Object.assign(settings, {
    platformName: "MaticTrade",
    supportEmail: "support@matictrade.com",
    maintenanceMode: false,
    emailNotifications: true,
    telegramNotifications: false,
  });

  toast.success("Settings reset to defaults");
};

// Load settings from backend
const loadSettings = async () => {
  isLoading.value = true;
  try {
    const data = await adminStore.getSystemSettings();
    if (data) {
      settings.platformName = data.platformName || "MaticTrade";
      settings.supportEmail = data.supportEmail || "support@matictrade.com";
      settings.maintenanceMode = data.maintenanceMode || false;
      settings.emailNotifications = data.emailNotifications ?? true;
      settings.telegramNotifications = data.telegramNotifications || false;
      webhookToken.value = data.webhookToken || "";
    }
    
    // Load social links
    await loadSocialLinks();
    
    // Load homepage settings
    await loadHomepageSettings();
  } catch (error: any) {
    console.error("Failed to load settings:", error);
  } finally {
    isLoading.value = false;
  }
};

// Load social links from backend
const loadSocialLinks = async () => {
  try {
    const baseUrl = config.public.apiBaseUrl;
    const response = await fetch(`${baseUrl}/api/settings/social`);
    const data = await response.json();
    if (data.success) {
      Object.assign(socialLinks, data.data);
    }
  } catch (error) {
    console.error("Failed to load social links:", error);
  }
};

// Save social links to backend
const saveSocialLinks = async () => {
  try {
    const token = localStorage.getItem('token');
    const baseUrl = config.public.apiBaseUrl;
    const response = await fetch(`${baseUrl}/api/settings/social`, {
      method: 'PUT',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${token}`
      },
      body: JSON.stringify(socialLinks)
    });
    const data = await response.json();
    if (data.success) {
      toast.success("Social links saved successfully");
    } else {
      throw new Error(data.message);
    }
  } catch (error: any) {
    toast.error(error.message || "Failed to save social links");
  }
};

// Load homepage settings from backend
const loadHomepageSettings = async () => {
  try {
    const baseUrl = config.public.apiBaseUrl;
    const response = await fetch(`${baseUrl}/api/settings/homepage`);
    const data = await response.json();
    if (data.success) {
      Object.assign(homepageSettings, data.data);
    }
  } catch (error) {
    console.error("Failed to load homepage settings:", error);
  }
};

// Save homepage settings to backend
const saveHomepageSettings = async () => {
  try {
    const token = localStorage.getItem('token');
    const baseUrl = config.public.apiBaseUrl;
    const response = await fetch(`${baseUrl}/api/settings/homepage`, {
      method: 'PUT',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${token}`
      },
      body: JSON.stringify(homepageSettings)
    });
    const data = await response.json();
    if (!data.success) {
      throw new Error(data.message);
    }
  } catch (error: any) {
    toast.error(error.message || "Failed to save homepage settings");
  }
};

// Initialize on mount
onMounted(() => {
  loadSettings();
});
</script>
